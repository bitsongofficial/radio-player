<template>
  <v-list-item class="px-0">
    <v-img
      max-width="32"
      max-height="32"
      :src="item.picture"
      :streamUrl="item.stream_url"
      class="mr-4 ml-2"
      v-if="item.picture"
      @click.stop="onClick(item)"
    ></v-img>
    <v-btn class="mr-4 ml-1" v-else icon @click.stop="onClick(item)">
      <v-icon size="22" color="grey"> mdi-podcast </v-icon>
    </v-btn>
    <v-list-item-content>
      <v-list-item-title
        class="font-weight-medium"
        v-html="item.title"
        @click="onClick(item)"
      >
      </v-list-item-title>
      <v-list-item-subtitle class="grey--text">
        {{ item.subtitle | stripHtml }}
      </v-list-item-subtitle>
    </v-list-item-content>

    <v-list-item-action class="text-right">
      <v-btn icon @click.stop="onFavorite(item)">
        <v-icon color="red darken-4" v-if="isFavorite(item)">mdi-heart</v-icon>
        <v-icon color="grey" v-else>mdi-heart-outline</v-icon>
      </v-btn>
    </v-list-item-action>
    <v-list-item-action class="text-right ml-0">
      <v-btn icon color="grey">
        <v-icon>mdi-dots-vertical</v-icon>
      </v-btn>
    </v-list-item-action>
  </v-list-item>
</template>

<script>
export default {
  props: {
    item: {
      type: Object,
      default() {
        return {};
      }
    }
  },

  methods: {
    onClick(item) {
      const track = {
        ...item,
        type: "podcast"
      };
      this.$store.dispatch(`player/setCurrentTrack`, track);
    },
    onFavorite(item) {
      if (!this.isFavorite(item)) {
        this.$store.dispatch(`favorites/addRadio`, item);
      } else {
        this.$store.dispatch(`favorites/removeRadio`, item);
      }
    },
    isFavorite(item) {
      return this.$store.getters["favorites/radios"].find(
        r => r.stream_url === item.stream_url
      )
        ? true
        : false;
    }
  }
};
</script>
